package week10d01;

public class HikingStat {

    private double ascent;
    private double descent;

    public HikingStat(double ascent, double descent) {
        this.ascent = ascent;
        this.descent = descent;
    }

    public double getAscent() {
        return ascent;
    }

    public double getDescent() {
        return descent;
    }
}
